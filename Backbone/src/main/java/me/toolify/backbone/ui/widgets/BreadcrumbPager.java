    /**
     * Layout parameters that should be supplied for views added to a
     * BreadcrumbPager.
     */
    public static class LayoutParams extends ViewGroup.LayoutParams {
        /**
         * true if this view is a decoration on the pager itself and not
         * a view supplied by the adapter.
         */
        public boolean isDecor;

        /**
         * Gravity setting for use on decor views only:
         * Where to position the view page within the overall BreadcrumbPager
         * container; constants are defined in {@link android.view.Gravity}.
         */
        public int gravity;

        /**
         * Width as a 0-1 multiplier of the measured pager width
         */
        float widthFactor = 0.f;

        /**
         * true if this view was added during layout and needs to be measured
         * before being positioned.
         */
        boolean needsMeasure;

        /**
         * Adapter position this view is for if !isDecor
         */
        int position;

        /**
         * Current child index within the BreadcrumbPager that this view occupies
         */
        int childIndex;

        public LayoutParams() {
            super(MATCH_PARENT, MATCH_PARENT);
        }

        public LayoutParams(Context context, AttributeSet attrs) {
            super(context, attrs);

            final TypedArray a = context.obtainStyledAttributes(attrs, LAYOUT_ATTRS);
            gravity = a.getInteger(0, Gravity.TOP);
            a.recycle();
        }
    }

    static class ViewPositionComparator implements Comparator<View> {
        @Override
        public int compare(View lhs, View rhs) {
            final LayoutParams llp = (LayoutParams) lhs.getLayoutParams();
            final LayoutParams rlp = (LayoutParams) rhs.getLayoutParams();
            if (llp.isDecor != rlp.isDecor) {
                return llp.isDecor ? 1 : -1;
            }
            return llp.position - rlp.position;
        }
    }

    private class PageListener implements ViewPager.OnPageChangeListener {

        @Override
        public void onPageScrolled(int position, float positionOffset, int positionOffsetPixels) {

            mCurrentPage = position;
            mPositionOffset = positionOffset;

            // We can assume that all items have the same "width" (meaning that each breadcrumb
            // "page" has the same width as the widest breadcrumb, even each individual breadcrumb
            // doesn't take up the full width.  Therefore, in order to achieve smooth scrolling
            // we multiply the position offset by our assumed pager width
            scrollToChild(position, (int) (positionOffset * mGreatestChildWidth));

            if (mDelegatePageListener != null) {
                mDelegatePageListener.onPageScrolled(position, positionOffset, positionOffsetPixels);
            }
        }

        @Override
        public void onPageScrollStateChanged(int state) {
            if (state == ViewPager.SCROLL_STATE_IDLE) {
                setCurrentItem(pager.getCurrentItem());
            }

            if (mDelegatePageListener != null) {
                mDelegatePageListener.onPageScrollStateChanged(state);
            }
        }

        @Override
        public void onPageSelected(int position) {
            if (mDelegatePageListener != null) {
                mDelegatePageListener.onPageSelected(position);
            }
        }

    }

    private class BreadcrumbPagerAdapter extends PagerAdapter {

        private int mNumBreadcrumbs;
        private Context mContext;

        public BreadcrumbPagerAdapter(Context context) {
            this.mContext = context;
            this.mNumBreadcrumbs = FileManagerApplication.NUM_PAGES;
        }

        /**
         * Get a View that displays the data at the specified position in the data set.
         *
         * @param position The position of the item within the adapter's data set of the item whose view we want.
         * @param pager    The ViewPager that this view will eventually be attached to.
         *
         * @return A View corresponding to the data at the specified position.
         */
        public View getView(int position, BreadcrumbPager pager) {
            return LayoutInflater.from(this.mContext).inflate(R.layout.breadcrumb_spinner, null, false);
        }

        /**
         * Determines whether a page View is associated with a specific key breadcrumb as
         * returned by instantiateItem(ViewGroup, int).
         *
         * @param view   Page View to check for association with breadcrumb
         * @param object Object to check for association with view
         *
         * @return true if view is associated with the key breadcrumb breadcrumb.
         */
        @Override
        public boolean isViewFromObject(View view, Object object) {
            return view == object;
        }

        @Override
        public int getCount() {
            return mNumBreadcrumbs;
        }

        /**
         * Create the page for the given position.
         *
         * @param container The containing View in which the page will be shown.
         * @param position  The page position to be instantiated.
         *
         * @return Returns an Object representing the new page. This does not need
         *         to be a View, but can be some other container of the page.
         */
        @Override
        public Object instantiateItem(ViewGroup container, int position) {
            BreadcrumbPager pager = (BreadcrumbPager) container;
            View view = getView(position, pager);

            pager.addView(view);

            // Check to see if a fragment/breadcrumb has been queued (when the fragment loads before
            // the corresponding breadcrumb does.
            if (mQueuedFragments.containsKey(position)) {
                mQueuedFragments.get(position).setBreadcrumb((Breadcrumb) view);
            }

            return view;
        }

        /**
         * Remove a page for the given position.
         *
         * @param container The containing View from which the page will be removed.
         * @param position  The page position to be removed.
         * @param view      The same breadcrumb that was returned by instantiateItem(View, int).
         */
        @Override
        public void destroyItem(ViewGroup container, int position, Object view) {
            ((BreadcrumbPager) container).removeView((View) view);
        }
    }
